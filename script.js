function renderLogo() {
  const canvas = document.getElementById("logoCanvas");
  const image = document.getElementById("logoSource");
  if (!canvas || !image) return;
  if (window.getComputedStyle(canvas).display === "none") return;

  const context = canvas.getContext("2d");
  const draw = () => {
    const size = Math.min(canvas.width, canvas.height);
    const inset = 6;
    const radius = size / 2 - inset;

    context.clearRect(0, 0, canvas.width, canvas.height);
    context.save();
    context.beginPath();
    context.arc(size / 2, size / 2, radius, 0, Math.PI * 2);
    context.clip();
    context.drawImage(image, inset, inset, size - inset * 2, size - inset * 2);
    context.restore();
  };

  if (image.complete) {
    draw();
  } else {
    image.addEventListener("load", draw, { once: true });
  }
}

function setupReveals() {
  const items = document.querySelectorAll(".reveal");
  if (!items.length) return;

  if (reduceMotion || !("IntersectionObserver" in window)) {
    items.forEach((item) => item.classList.add("revealed"));
    return;
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("revealed");
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.15, rootMargin: "0px 0px -40px 0px" });

  items.forEach((item) => observer.observe(item));
}

function typeHero() {
  const element = document.querySelector("[data-typing]");
  if (!element) return;

  const text = element.dataset.typingText || element.textContent.trim();
  window.clearTimeout(typingTimer);

  if (reduceMotion) {
    element.textContent = text;
    element.classList.remove("is-typing");
    return;
  }

  element.textContent = "";
  element.classList.add("is-typing");
  let index = 0;

  const step = () => {
    element.textContent = text.slice(0, index);
    index += 1;
    if (index <= text.length) {
      typingTimer = window.setTimeout(step, 38);
    } else {
      element.classList.remove("is-typing");
    }
  };

  step();
}

function setupParticles() {
  const canvas = document.getElementById("particlesLayer");
  if (!canvas || reduceMotion) return;
  const context = canvas.getContext("2d");
  if (!context) return;

  let animationFrame = 0;
  let particles = [];

  const createParticles = () => {
    const count = Math.min(38, Math.floor(window.innerWidth / 38));
    particles = Array.from({ length: count }, () => ({
      x: Math.random() * canvas.width,
      y: Math.random() * canvas.height,
      r: Math.random() * 1.8 + 0.8,
      speed: Math.random() * 0.45 + 0.2,
      drift: (Math.random() - 0.5) * 0.2,
      alpha: Math.random() * 0.4 + 0.15
    }));
  };

  const resize = () => {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
    createParticles();
  };

  const draw = () => {
    context.clearRect(0, 0, canvas.width, canvas.height);
    particles.forEach((particle) => {
      particle.y -= particle.speed;
      particle.x += particle.drift;

      if (particle.y < -10) {
        particle.y = canvas.height + 10;
        particle.x = Math.random() * canvas.width;
      }
      if (particle.x < -10) particle.x = canvas.width + 10;
      if (particle.x > canvas.width + 10) particle.x = -10;

      context.beginPath();
      context.fillStyle = `rgba(216, 189, 132, ${particle.alpha})`;
      context.arc(particle.x, particle.y, particle.r, 0, Math.PI * 2);
      context.fill();
    });

    animationFrame = window.requestAnimationFrame(draw);
  };

  resize();
  draw();
  window.addEventListener("resize", resize);
  window.addEventListener("beforeunload", () => window.cancelAnimationFrame(animationFrame), { once: true });
}

function setupForm() {
  const form = document.getElementById("join-form");
  const feedback = document.getElementById("joinFormFeedback");
  if (!form || !feedback) return;

  const fields = Array.from(form.querySelectorAll("[data-field]"));

  const markField = (field) => {
    const wrapper = field.closest(".form-field");
    if (!wrapper) return;
    wrapper.classList.toggle("is-invalid", !field.checkValidity());
  };

  fields.forEach((field) => {
    field.addEventListener("input", () => {
      markField(field);
      if (field.checkValidity()) {
        feedback.textContent = "";
        delete feedback.dataset.state;
      }
    });
  });

  form.addEventListener("submit", (event) => {
    const invalid = fields.find((field) => !field.checkValidity());
    fields.forEach(markField);

    if (invalid) {
      event.preventDefault();
      feedback.dataset.state = "error";
      feedback.textContent = pick(translations[getLang()], fieldErrorKey(invalid.dataset.field)) || "";
      invalid.focus();
      return;
    }

    let cachedPayload = null;

    try {
      const previousMessages = JSON.parse(localStorage.getItem(joinMessagesStorageKey) || "[]");
      const nextMessages = Array.isArray(previousMessages) ? previousMessages : [];
      cachedPayload = {
        name: form.elements.name?.value?.trim() || "",
        email: form.elements.email?.value?.trim() || "",
        message: form.elements.message?.value?.trim() || "",
        language: getLang(),
        createdAt: new Date().toISOString(),
        source: "join-form"
      };
      nextMessages.push(cachedPayload);

      if (nextMessages.length > 250) {
        nextMessages.splice(0, nextMessages.length - 250);
      }

      localStorage.setItem(joinMessagesStorageKey, JSON.stringify(nextMessages));
    } catch (error) {
      // Keep submission flow even if local cache fails.
    }

    if (cachedPayload) {
      mirrorJoinMessageToSupabase({
        name: cachedPayload.name,
        email: cachedPayload.email,
        message: cachedPayload.message,
        language: cachedPayload.language,
        source: cachedPayload.source,
        page_path: window.location.pathname || "/join.html",
        user_agent: String(navigator.userAgent || "").slice(0, 255),
        created_at: cachedPayload.createdAt
      });
    }

    feedback.dataset.state = "success";
    feedback.textContent = pick(translations[getLang()], "common.formSuccess") || "";
  });
}

function setupQuoteModal() {
  const fab = document.getElementById("quoteFab");
  const modal = document.getElementById("quoteModal");
  if (!fab || !modal) return;

  const footer = document.querySelector(".footer");

  const syncFab = () => {
    if (!footer) return;
    const overlap = Math.max(0, window.innerHeight - footer.getBoundingClientRect().top + 16);
    fab.style.bottom = `${24 + overlap}px`;
  };

  const open = () => {
    refreshQuote(true);
    modal.hidden = false;
    document.body.classList.add("gate-open");
  };

  const close = () => {
    modal.hidden = true;
    if (!document.querySelector(".language-gate")) {
      document.body.classList.remove("gate-open");
    }
  };

  syncFab();
  window.addEventListener("scroll", syncFab, { passive: true });
  window.addEventListener("resize", syncFab);
  fab.addEventListener("click", open);

  modal.querySelectorAll("[data-close-quote]").forEach((element) => {
    element.addEventListener("click", close);
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !modal.hidden) close();
  });
}

