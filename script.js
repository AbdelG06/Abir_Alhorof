function pick(object, path) {
  return path.split(".").reduce((acc, key) => (acc ? acc[key] : undefined), object);
}

function getLang() {
  return sessionStorage.getItem(langKey) || "fr";
}

function setLang(next) {
  sessionStorage.setItem(langKey, next);
}

function setDirection(next) {
  const isArabic = next === "ar";
  document.documentElement.lang = isArabic ? "ar" : "fr";
  document.documentElement.dir = isArabic ? "rtl" : "ltr";
  document.body.classList.toggle("lang-ar", isArabic);
  document.body.classList.toggle("lang-fr", !isArabic);
}

function setLangButton(next) {
  const current = document.querySelector("[data-lang-current]");
  const target = document.querySelector("[data-lang-target]");
  if (!current || !target) return;
  current.textContent = next.toUpperCase();
  target.textContent = next === "ar" ? "FR" : "AR";
}

function fieldErrorKey(fieldName) {
  if (fieldName === "name") return "common.formName";
  if (fieldName === "email") return "common.formEmail";
  return "common.formMessage";
}

function refreshFormMessage() {
  const form = document.getElementById("join-form");
  const feedback = document.getElementById("joinFormFeedback");
  if (!form || !feedback || feedback.dataset.state !== "error") return;
  const invalid = form.querySelector("[data-field]:invalid");
  if (!invalid) return;
  feedback.textContent = pick(translations[getLang()], fieldErrorKey(invalid.dataset.field)) || "";
}

function refreshQuote(force) {
  const text = document.getElementById("quoteModalText");
  const author = document.getElementById("quoteModalAuthor");
  if (!text || !author) return;

  if (force || !quotePool[currentQuote]) {
    let next = Math.floor(Math.random() * quotePool.length);
    if (quotePool.length > 1 && next === currentQuote) {
      next = (next + 1) % quotePool.length;
    }
    currentQuote = next;
  }

  const current = getLang();
  const quote = quotePool[currentQuote];
  text.textContent = current === "ar" ? quote.ar : quote.fr;
  author.textContent = quote.author;
}

function applyTranslations(next) {
  const dictionary = translations[next];
  if (!dictionary) return;

  setDirection(next);
  setLangButton(next);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = pick(dictionary, element.dataset.i18n);
    if (typeof value === "string") element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const value = pick(dictionary, element.dataset.i18nHtml);
    if (typeof value === "string") element.innerHTML = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = pick(dictionary, element.dataset.i18nPlaceholder);
    if (typeof value === "string") element.setAttribute("placeholder", value);
  });

  const title = dictionary.meta?.titles?.[page];
  if (title) document.title = title;

  const typingElement = document.querySelector("[data-typing]");
  if (typingElement) {
    typingElement.dataset.typingText = pick(dictionary, typingElement.dataset.i18n) || typingElement.textContent.trim();
  }

  refreshQuote(false);
  refreshFormMessage();
}

function setActiveNavigation() {
  document.querySelectorAll("[data-nav]").forEach((link) => {
    const poemListing = page.startsWith("poem") && link.dataset.nav === "poems";
    link.classList.toggle("is-active", link.dataset.nav === page || poemListing);
  });
}

function setupNavigation() {
  const toggle = document.querySelector(".nav-toggle");
  const panel = document.querySelector(".nav-panel");
  if (!toggle || !panel) return;

  toggle.addEventListener("click", () => {
    const open = panel.classList.toggle("is-open");
    toggle.setAttribute("aria-expanded", String(open));
  });

  panel.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      panel.classList.remove("is-open");
      toggle.setAttribute("aria-expanded", "false");
    });
  });
}

function setupLanguageSwitch() {
  const button = document.querySelector(".lang-switch");
  if (!button) return;
  if (isPoemPage) {
    button.hidden = true;
    return;
  }

  button.addEventListener("click", () => {
    const next = getLang() === "fr" ? "ar" : "fr";
    setLang(next);
    applyTranslations(next);
    typeHero();
  });
}

